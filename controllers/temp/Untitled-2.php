<h4>- Ojos verdes:</h4>
<p>Contraste: marrones, morados, violetas y rojos, porque son colo-        res contrarios al verde.</p>
<p>Armon&iacute;a: verdes m&aacute;s oscuros que el color del iris para crear extras profundidad y tonos mas claros para dar luz.</p>
<h4>- Ojos azules-grisaceos:</h4>
<p>Contraste: rosas, naranja, dorados, bronces, porque son tonos           contrarios al azul y gris.</p>
<p>Armon&iacute;a: colores parecidos al iris para resaltar el color, y tonos           m&aacute;s claros para iluminar la mirada.</p>
<h4>- Ojos marrones-negros:</h4>
<p>Favorecen pr&aacute;cticamente todos los colores.</p>
<p>Contraste: rojos, amarillo, platas y verdes; porque son colores          complementarios.</p>
<p>Armon&iacute;a: dorados, marrones, ocres y los tonos tierras en gene-           ral. Porque son de la misma gama crom&aacute;tica.</p>
<p>No obstante hay colores neutros que se pueden utilizar con                                              cualquier color de ojos, como los grises, beige, blanco y negro.</p>
